#!/usr/bin/env bash
# build/build.sh — concatenate the pure lib + the JXA src into one deployable
# JXA file: dist/eccl-calsync.js.
#
# osascript cannot require() Node modules, so we inline everything in dependency
# order: identity and reconcile (pure) first, then the eventkit adapter, then
# main (which auto-runs the CLI under osascript). The same pure files are
# required directly by the Node tests, so logic is built and tested without a Mac.

set -euo pipefail

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
OUT_DIR="$ROOT/dist"
OUT="$OUT_DIR/eccl-calsync.js"

mkdir -p "$OUT_DIR"

# Order matters: pure deps, then adapter, then entry (entry runs on load).
FILES=(
  "$ROOT/lib/identity.js"
  "$ROOT/lib/reconcile.js"
  "$ROOT/src/eventkit.js"
  "$ROOT/src/main.js"
)

{
  echo "// dist/eccl-calsync.js — BUILD OUTPUT, do not edit by hand."
  echo "// Generated by build/build.sh from lib/*.js + src/*.js."
  echo "// Run with: osascript -l JavaScript dist/eccl-calsync.js [list|once|once --dry-run]"
  echo ""
  for f in "${FILES[@]}"; do
    echo "// ===== $(basename "$(dirname "$f")")/$(basename "$f") ====="
    cat "$f"
    echo ""
  done
} > "$OUT"

# Validate the concatenated file parses as JavaScript before declaring success.
if command -v node >/dev/null 2>&1; then
  node --check "$OUT"
  echo "build: wrote $OUT and verified it parses (node --check)."
else
  echo "build: wrote $OUT (node not found, skipped --check)."
fi
